CREATE TABLE festivals ( 
    id BIGINT(11) UNSIGNED NOT NULL AUTO_INCREMENT, 
    title VARCHAR(100) NOT NULL, 
    description VARCHAR(255),
    city VARCHAR(30) NOT NULL, 
    start_date DATETIME NOT NULL, 
    end_date DATETIME NOT NULL, 
    image_path VARCHAR(100), 
    PRIMARY KEY (id) 
);

CREATE TABLE stages ( 
    id BIGINT(11) UNSIGNED NOT NULL AUTO_INCREMENT, 
    title VARCHAR(100) NOT NULL, 
    description VARCHAR(255), 
    location VARCHAR(20) NOT NULL, 
    festival_id BIGINT(11) UNSIGNED NOT NULL, 
    image_path VARCHAR(100), 
    PRIMARY KEY (id), 
    CONSTRAINT fk_stage_festival FOREIGN KEY (festival_id) REFERENCES festivals(id) ON UPDATE CASCADE ON DELETE CASCADE 
);

CREATE TABLE performers ( 
    id BIGINT(11) UNSIGNED NOT NULL AUTO_INCREMENT, 
    title VARCHAR(100) NOT NULL, 
    description VARCHAR(255), 
    contact_email VARCHAR(50) NOT NULL, 
    contact_phone VARCHAR(20) NOT NULL, 
    image_path VARCHAR(100),
    PRIMARY KEY (id)
);

CREATE TABLE shows (
    id BIGINT(11) UNSIGNED NOT NULL AUTO_INCREMENT, 
    start_time DATETIME NOT NULL, 
    end_time DATETIME NOT NULL, 
    stage_id BIGINT(11) UNSIGNED NOT NULL, 
    performer_id BIGINT(11) UNSIGNED NOT NULL, 
    PRIMARY KEY (id), 
    CONSTRAINT fk_show_stage FOREIGN KEY (stage_id) REFERENCES stages(id) ON UPDATE CASCADE ON DELETE CASCADE, 
    CONSTRAINT fk_show_performer FOREIGN KEY (performer_id) REFERENCES performers(id) ON UPDATE CASCADE ON DELETE CASCADE 
);


